// Script variant

#include "CVariant.h"

namespace rev
{
	//------------------------------------------------------------------------------------------------------------------
	CVariant::CVariant()
		:mType(eNill)
	{
	}

	//------------------------------------------------------------------------------------------------------------------
	CVariant::CVariant(const CVariant& _x)
		:mType(_x.mType)
		,mData(_x.mData)
	{
	}

	//------------------------------------------------------------------------------------------------------------------
	CVariant::CVariant(int _i)
		:mType(eInteger)
	{
		mData.i = _i;
	}

	//------------------------------------------------------------------------------------------------------------------
	CVariant::CVariant(double _d)
		:mType(eReal)
	{
		mData.d = _d;
	}

	//------------------------------------------------------------------------------------------------------------------
	CVariant::CVariant(bool _b)
		:mType(eBool)
	{
		mData.b = _b;
	}

	//------------------------------------------------------------------------------------------------------------------
	CVariant& CVariant::operator=(const CVariant& _x)
	{
		mType = _x.mType;
		mData = _x.mData;
		return *this;
	}

	//------------------------------------------------------------------------------------------------------------------
	CVariant& CVariant::operator=(int _x)
	{
		mType = eInteger;
		mData.i = _x;
		return *this;
	}
}	// namespace rev
